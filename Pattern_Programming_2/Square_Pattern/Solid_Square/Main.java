package Pattern_Programming_2.Square_Pattern.Solid_Square;

public class Main {
    public static void main(String[] args) {
       
        int n=5; 
        for(int i=1; i<=n; i++){
           for(int j=1; j<=n; j++){
               System.out.print("* ");
           }
           System.out.println();
       }
    }
}
